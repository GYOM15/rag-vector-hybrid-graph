"""Evaluation dashboard: visualizes the eval results (committed snapshots).

Reads the reference snapshots in `eval/reference/` (version-controlled, unlike the
scratch outputs `eval/*.json`) and renders them as tables + charts. **No heavy compute
here** — like MLflow / Weights & Biases, we *visualize already-computed runs*. The heavy
evals are re-run from the command line (README §4); the snapshots are regenerated by
copying their outputs into `eval/reference/`.
"""

import json
from pathlib import Path

import streamlit as st

_ROOT = Path(__file__).resolve().parent.parent
REFERENCE = _ROOT / "eval" / "reference"
PERF_PARETO = _ROOT / "docs" / "perf-pareto.svg"

_STACK_COLORS = ["#3b82f6", "#22c55e", "#a855f7"]  # blue, green, violet
_ORDER = ["Vector", "Hybrid", "Graph"]


def _short(name: str) -> str:
    n = name.lower()
    return "Vector" if "vecto" in n else ("Hybrid" if "hybr" in n else "Graph")


def _load(name: str) -> dict | None:
    path = REFERENCE / name
    return json.loads(path.read_text("utf-8")) if path.exists() else None


def _cols(df):
    """Keep and order the architecture columns (Vector/Hybrid/Graph)."""
    return df[[c for c in _ORDER if c in df.columns]]


def _rows(df):
    """Order the architecture rows."""
    return df.reindex([c for c in _ORDER if c in df.index])


def _grouped_bar(matrix, x_title: str, y_title: str, title: str, value_format: str = ".2f") -> None:
    """Grouped bars (Altair). `matrix`: index = X axis, columns = series."""
    import altair as alt

    order = list(matrix.columns)
    frame = matrix.copy()
    frame.index = frame.index.astype(str)
    frame.index.name = x_title
    long = (frame.reset_index()
            .melt(id_vars=x_title, var_name="Series", value_name=y_title)
            .dropna(subset=[y_title]))
    if long.empty:
        st.info("No data to plot.")
        return
    base = alt.Chart(long).encode(
        x=alt.X(f"{x_title}:N", title=x_title, axis=alt.Axis(labelAngle=0)),
        xOffset=alt.XOffset("Series:N", sort=order),
        y=alt.Y(f"{y_title}:Q", title=y_title),
        color=alt.Color("Series:N",
                        scale=alt.Scale(domain=order, range=_STACK_COLORS[: len(order)]),
                        legend=alt.Legend(orient="bottom", title=None)),
        tooltip=[alt.Tooltip(f"{x_title}:N"), alt.Tooltip("Series:N"),
                 alt.Tooltip(f"{y_title}:Q", format=value_format)],
    )
    bars = base.mark_bar()
    labels = base.mark_text(dy=-4, fontSize=10, color="#94a3b8").encode(
        text=alt.Text(f"{y_title}:Q", format=value_format))
    st.altair_chart((bars + labels).properties(title=title, height=320), width="stretch")


# --- Retrieval (BEIR) ---------------------------------------------------------

_BEIR = {"SciFact": "beir_scifact.json", "NFCorpus": "beir_nfcorpus.json",
         "HotpotQA": "beir_hotpotqa.json"}


def render_beir() -> None:
    import pandas as pd

    loaded = {label: d for label, f in _BEIR.items() if (d := _load(f))}
    if not loaded:
        st.info("No BEIR snapshot in `eval/reference/`. "
                "Generate them with `python -m eval.beir_eval …` then copy them there.")
        return

    st.markdown("**Retrieval quality** — nDCG@10, *no LLM*, human relevance judgments (qrels).")
    ndcg = {ds: {_short(k): v["ndcg@10"] for k, v in d["stacks"].items()} for ds, d in loaded.items()}
    _grouped_bar(_cols(pd.DataFrame(ndcg).T), "Dataset", "nDCG@10",
                 "nDCG@10 by dataset (higher = better)", ".3f")
    st.caption("Hybrid wins on all 3 corpora. Graph trails on SciFact/HotpotQA but edges out "
               "Vector on the entity-heavy NFCorpus (consistent with its named-entity strength) "
               "— though it stays the costliest index to build.")

    st.divider()
    ds = st.selectbox("Dataset detail", list(loaded.keys()), key="beir_ds")
    d = loaded[ds]
    c = d["config"]
    st.caption(f"{c.get('n_docs', '?')} docs · {c.get('n_queries', '?')} queries · "
               f"embedder {c.get('embedder', '?')}")
    metrics = ["recall@1", "recall@5", "recall@10", "ndcg@10", "mrr"]
    table = {_short(k): {m: v.get(m) for m in metrics} for k, v in d["stacks"].items()}
    st.dataframe(_rows(pd.DataFrame(table).T)[metrics], width="stretch")


# --- Reranking ----------------------------------------------------------------

_RERANK = {"SciFact": "rerank_scifact.json", "NFCorpus": "rerank_nfcorpus.json",
           "HotpotQA": "rerank_hotpotqa.json"}


def render_reranking() -> None:
    import pandas as pd

    loaded = {label: d for label, f in _RERANK.items() if (d := _load(f))}
    if not loaded:
        st.info("No reranking snapshot in `eval/reference/`.")
        return

    st.markdown("**Cross-encoder reranking** — *replace* (follow the reranker alone) "
                "vs *fuse* (RRF of the base rank + the reranker rank).")
    mean_delta = {}
    for ds, d in loaded.items():
        vals = list(d["stacks"].values())
        n = len(vals)
        mean_delta[ds] = {"replace": round(sum(v["delta_replace"] for v in vals) / n, 3),
                          "fuse (RRF)": round(sum(v["delta_fusion"] for v in vals) / n, 3)}
    _grouped_bar(pd.DataFrame(mean_delta).T[["replace", "fuse (RRF)"]], "Dataset",
                 "Mean Δ nDCG@10", "Mean reranking gain (the winner changes by dataset)", ".3f")
    st.caption("Fusion **does not generalize**: it only wins on SciFact (where *replace* "
               "hurts the strong Hybrid). On NFCorpus/HotpotQA, *replace* wins. → measure per dataset.")

    st.divider()
    ds = st.selectbox("Dataset detail", list(loaded.keys()), key="rerank_ds")
    table = {_short(k): {"without": v["ndcg_base"], "replace": v["ndcg_replace"], "fuse": v["ndcg_fusion"]}
             for k, v in loaded[ds]["stacks"].items()}
    df = _cols(pd.DataFrame(table)).reindex(["without", "replace", "fuse"])
    _grouped_bar(df, "Variant", "nDCG@10", f"{ds} — without / replace / fuse by architecture", ".3f")
    st.dataframe(df, width="stretch")


# --- Systems ------------------------------------------------------------------

def render_systems() -> None:
    import pandas as pd

    d = _load("perf_scifact.json")
    if not d:
        st.info("No systems snapshot (`perf_scifact.json`).")
        return
    c = d["config"]
    st.markdown(f"**Cost & speed** — *no LLM*, {c.get('n_docs', '?')} docs, "
                f"{c.get('n_queries', '?')} queries, {c.get('repeats', '?')} repeats.")

    bs = d["build_seconds"]
    build = {"Vector": bs.get("vector_total"), "Hybrid": bs.get("hybrid_total"), "Graph": bs.get("graph_total")}
    _grouped_bar(_cols(pd.DataFrame({"Build (s)": build}).T), "Step", "Seconds",
                 "Index build cost (lower = better)", ".0f")
    st.caption(f"Graph spaCy NER ≈ {bs.get('graph_ner_build', 0):.0f}s — the most expensive index.")

    mem = d.get("build_memory_mb")
    if mem:
        st.divider()
        st.markdown(
            f"**Memory** — the FAISS vector store is ~{mem.get('faiss_vectors_mb', 0):.0f} MB; the whole "
            f"process peaks at ~{mem.get('process_peak_mb', 0):.0f} MB, dominated by the embedding model "
            f"+ spaCy runtime. At this corpus scale the per-index structures are tens of MB — so **memory "
            f"isn't the differentiator here; build time is** (the graph's NER pass). The networkx graph "
            f"grows with entity count, so memory would surface at larger scale.")

    st.divider()
    lat = {_short(k): {"median": v["latency"]["median_ms"], "p95": v["latency"]["p95_ms"],
                       "p99": v["latency"]["p99_ms"]} for k, v in d["stacks"].items()}
    _grouped_bar(_cols(pd.DataFrame(lat)).reindex(["median", "p95", "p99"]), "Percentile",
                 "Latency (ms)", "Latency per query (lower = better)", ".1f")

    st.divider()
    thr = {_short(k): v["throughput_qps"] for k, v in d["stacks"].items()}
    thr_df = pd.DataFrame(thr)
    thr_df.index = thr_df.index.astype(int)
    thr_df = _cols(thr_df.sort_index())
    st.markdown("**Throughput vs concurrency** (req/s) — only Vector scales (FAISS releases the GIL).")
    st.line_chart(thr_df, x_label="Concurrent queries", y_label="req/s")

    if PERF_PARETO.exists():
        st.divider()
        st.markdown("**Pareto frontier** quality × latency")
        st.image(str(PERF_PARETO))


# --- Answer quality -----------------------------------------------------------

def render_answer() -> None:
    import pandas as pd

    runs = {"llama3.2:1b": _load("answer_1b.json"), "llama3.2:3b": _load("answer_3b.json")}
    runs = {k: v for k, v in runs.items() if v}
    if not runs:
        st.info("No answer-quality snapshot.")
        return
    st.markdown("**End-to-end answer quality** — F1 (SQuAD-style) on HotpotQA gold "
                "answers, deterministic generation (temperature 0).")
    f1 = {model: {_short(k): v["f1"] for k, v in d["stacks"].items()} for model, d in runs.items()}
    _grouped_bar(_rows(pd.DataFrame(f1)), "Architecture", "F1",
                 "F1 by architecture and model", ".3f")
    st.caption("**Model size dominates** (3b ≫ 1b); the gaps *between architectures* "
               "are within noise (n=50) — at this local stage, the generator is the bottleneck.")


# --- Light evals, runnable live -----------------------------------------------

def _ensure_root_on_path() -> None:
    import sys

    if str(_ROOT) not in sys.path:
        sys.path.insert(0, str(_ROOT))


def render_regression_guard() -> None:
    st.markdown("**Regression guard** — nDCG@5 of the 3 stacks on the fixed *golden* corpus, "
                "the same check as in CI. Fails if a stack drops below `baseline − tolerance`.")
    if not st.button("Run the guard", type="primary", icon=":material/play_arrow:", key="run_guard"):
        return
    _ensure_root_on_path()
    import pandas as pd

    from eval.check_regression import BASELINES, check, measure

    with st.spinner("Building the 3 stacks on the golden corpus + nDCG@5… (~10 s)"):
        scores = measure()
    baseline = json.loads(BASELINES.read_text("utf-8"))
    failures = check(scores, baseline)

    tol = baseline["tolerance"]
    table = {arch: {"baseline": want, "current": scores.get(arch, 0.0),
                    "Δ": round(scores.get(arch, 0.0) - want, 4)}
             for arch, want in baseline["scores"].items()}
    st.dataframe(_rows(pd.DataFrame(table).T)[["baseline", "current", "Δ"]], width="stretch")
    if failures:
        detail = ", ".join(f"{a} {g:.3f} < {w}−{tol}" for a, w, g in failures)
        st.error(f"{len(failures)} regression(s) → CI would fail: {detail}")
    else:
        st.success(f"No regression (tolerance {tol}) — CI would pass.")


def render_type_retrieval(get_stacks) -> None:
    st.markdown("**Retrieval on the demo corpus, by question *type*** — hit@k + MRR, "
                "*no LLM*. Reuses the already-cached index (fast, no rebuild).")
    if not st.button("Evaluate retrieval", type="primary", icon=":material/play_arrow:", key="run_type"):
        return
    _ensure_root_on_path()
    import pandas as pd

    from eval.retrieval_eval import _eval_one

    data = [d for d in json.loads((_ROOT / "eval" / "questions.json").read_text("utf-8")) if d.get("gold")]
    types = [d.get("type", "?") for d in data]
    cats = sorted(set(types))
    with st.spinner("Retrieving the questions via the 3 architectures…"):
        report, _suspects = _eval_one(get_stacks(), data, types, cats, k_max=10)

    overall = {_short(n): r["overall"] for n, r in report.items()}
    st.dataframe(_rows(pd.DataFrame(overall).T)[["hit@1", "hit@3", "hit@5", "hit@10", "mrr"]],
                 width="stretch")
    for metric, label in (("hit@5", "hit@5"), ("mrr", "MRR")):
        mat = {t: {_short(n): r["by_type"].get(t, {}).get(metric) for n, r in report.items()} for t in cats}
        _grouped_bar(_cols(pd.DataFrame(mat).T), "Question type", label,
                     f"{label} by question type (higher = better)", ".2f")
